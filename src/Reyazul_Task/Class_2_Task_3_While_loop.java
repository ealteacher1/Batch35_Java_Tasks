package Reyazul_Task;

public class Class_2_Task_3_While_loop {

	public static void main(String[] args) {
		// print 2 to -10
				int a = 2;
				
				while (a>=-10) {
					System.out.println("a="+a);
					a--;
				}

				// print 3 to 20;
				int b = 3;
				
				while (b<=20) {
					System.out.println("b="+b);
					b++;
					
				}
				// print -355 to -255
				
				int c = -355;
				
				while (c<=-255) {
					System.out.println("c="+c);
					c++;
				}
				
				// print -956 to -960
				
				int d = -956;
				
				while (d>=-960) {
					System.out.println("d="+d);
					d--;
				}
				
				// print -10 to 10 
				int e = -10;
				while(e<=10) {
					System.out.println("e="+e);
					e++;
				}
				// print -5 to 5 and break if the value is 3;
				int f = -5;
				while(f<=5) {
					System.out.println("f="+f);
					
				if (f==3) {
					System.out.println("we got the target");
					break;
					
				}
				f++;
				
				}
				
				// print -500 to -200 and break if the value of the variable is -278
				int g = -200;
				while (g>=-500) {
					System.out.println("g="+g);
				if (g==-278) {
					System.out.println("Fill up");
					break;
					
				}
				g--;

	}
	}
}
