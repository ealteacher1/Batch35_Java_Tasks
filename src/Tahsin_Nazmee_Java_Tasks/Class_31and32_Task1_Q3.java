package Tahsin_Nazmee_Java_Tasks;

public class Class_31and32_Task1_Q3 {

	public static void main(String[] args) {
		// Print -355 to -255 using While loop
		int a = -355;
		while (a<=-255) {
			System.out.println("a="+a);
			a++;
		}
		
		// Print -355 to -255 using Do-While loop
		int b = -355;
		do {
			System.out.println("b="+b);
			b++;
		} while (b<=-255);
		
		// Print -355 to -255 using For loop
		for (int c=-355; c<=-255; c++) {
			System.out.println("c="+c);
		}
		
		
		/* End */
	}
}
