package Tahsin_Nazmee_Java_Tasks;

public class Class_31and32_Task1_Q4 {

	public static void main(String[] args) {
		// Print -956 to -960 using While loop
		int a = -956;
		while (a>=-960) {
			System.out.println("a="+a);
			a--;
		}
		
		// Print -956 to -960 using Do-While loop
		int b = -956;
		do {
			System.out.println("b="+b);
			b--;
		} while (b>=-960);
		
		// Print -956 to -960 using For loop
		for (int c=-956; c>=-960; c--) {
			System.out.println("c="+c);
		}
		
		/* End */
	}
}
