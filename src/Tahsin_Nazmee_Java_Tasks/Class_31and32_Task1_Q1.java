package Tahsin_Nazmee_Java_Tasks;

public class Class_31and32_Task1_Q1 {

	public static void main(String[] args) {
		// Print 2 to -10 using While loop
		int a = 2;
		while (a>=-10) {
			System.out.println("a="+a);
			a--;
		}
				
		// Print 2 to -10 using Do-While loop
		int b = 2;
		do {
			System.out.println("b="+b);
			b--;
		} while (b>=-10);
				
		// Print 2 to -10 using For loop
		for (int c=2; c>=-10; c--) {
			System.out.println("c="+c);
		}
				
		/* End */
	}
}
