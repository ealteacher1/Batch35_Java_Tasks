public class Class_29and30_Task2_CalculateAverage {

	public static void main(String[] args) {
		// Calculating Average
		/* 
		- Math = 60
		- Science = 66
		- Physics = 48
		- Biology = 74
		Task: Calculate Average
		*/
		int math = 60;
		int science = 66;
		int physics = 48;
		int biology = 74;
		int total_grade = math + science + physics + biology;
		float Average = total_grade/4f;
		System.out.println("Average: " + Average);
		
		/* End */
	}
}
