package Tahsin_Nazmee_Java_Tasks;

public class Class_31and32_Task1_Q2 {

	public static void main(String[] args) {
		// Print 3 to 20 using While loop
		int a = 3;
		while (a<=20) {
			System.out.println("a="+a);
			a++;
		}
		
		// Print 3 to 20 using Do-While loop
		int b = 3;
		do {
			System.out.println("b="+b);
			b++;
		} while (b<=20);
		
		// Print 3 to 20 using For loop
		for (int c=3; c<=20; c++) {
			System.out.println("c="+c);
		}
		
		/* End */		
	}
}
