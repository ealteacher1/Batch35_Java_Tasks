package Revision;

public class Task04_Java46_47 {

	public static void main(String[] args) {
		
		
		// Convert from Integer into String
		int x=100;
		String a=String.valueOf(x);
		System.out.println(a);
		
		//Convert true from boolean to string 
		
		boolean xx=true;
		String b=String.valueOf(xx);
		System.out.println(b);
		
		//Convert from '@' from character to string
		char xxx='@';
		String c=String.valueOf(xxx);
		System.out.println(c);
		
		//Convert from 20.00 from double to string
		double xxxx=20.00d;
		String d=String.valueOf(xxxx);
		System.out.println(d);

	}

}
