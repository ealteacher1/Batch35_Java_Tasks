package Revision;

public class Wrapper {

	public static void main(String[] args) {
		//Task 05_Java_46_47
		
		
		// convert double to double object
		double a= 12.22;
		
		Double ob= new Double(a);
		System.out.println(ob);
		
		//Convert char'#' to character object
		
		char b='#';
		Character ob1= new Character(b);
        System.out.println(ob1);
        
        //Convert int 500 to Integer object
        
        int c=500;
        Integer ob2=new Integer(c);
        System.out.println(ob2);
        
        //Convert boolean false to boolean object
        boolean d=false;
        Boolean ob3=new Boolean(d);
        System.out.println(ob3);
        
        
	}

}
