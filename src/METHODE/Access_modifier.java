package METHODE;

public class Access_modifier {

	    // Public static method
	    public static void publicStaticMethod() {
	        System.out.println("Public static method called.");
	    }
	    
	    // Public non-static method
	    public void publicNonStaticMethod() {
	        System.out.println("Public non-static method called.");
	    }
	    
	    // Private static method
	    private static void privateStaticMethod() {
	        System.out.println("Private static method called.");
	    }
	    
	    // Private non-static method
	    private void privateNonStaticMethod() {
	        System.out.println("Private non-static method called.");
	    }
	    
	    // Protected static method
	    protected static void protectedStaticMethod() {
	        System.out.println("Protected static method called.");
	    }
	    
	    // Protected non-static method
	    protected void protectedNonStaticMethod() {
	        System.out.println("Protected non-static method called.");
	    }
	    
	    // Default static method
	    static void defaultStaticMethod() {
	        System.out.println("Default static method called.");
	    }
	    
	    // Default non-static method
	    void defaultNonStaticMethod() {
	        System.out.println("Default non-static method called.");
	    }
	}
