package suzy_java_task;

public class Class02_task1_for_loop {
	public static void main(String[] args) {


	

	
//	print 2 to -10

    for (int a = 2; a>=-10; a--) {
        System.out.println("smile "+a);
    }

    // print 3 to 20;
    for (int b = 3; b<=20; b++) {
    System.out.println("Laugh "+b);

    }

    // print -355 to -255;
    for (int c = -355; c<=-255; c++) {
        System.out.println("Cry "+c);

    }
  //print -956 to -960
    for (int d =-956; d>=-960; d--) {
        System.out.println("Giggle"+d);

    }
    // print -10 to 10;
    for(int e =-10; e<=10; e++) {
        System.out.println("Race "+e);
    }

    // print -5 to 5 and break if the value is 3;

    for (int f = -5; f<=5; f++) {
        System.out.println("Fun"+f);

        if (f==3) {
            System.out.println(" good mood ");
            break;
        }
    }
    // print -500 to -200 and break if the value of the variable is -278;
    for (int g=-500; g<=-200; g++) {
        System.out.println("Funky "+g);
        if (g == -278) {
            System.out.println("happy place  ");
            break;
        }
    }
    }
}

    
	

	